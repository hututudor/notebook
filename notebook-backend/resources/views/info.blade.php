Welcome, {{ $user }}! Here is a quick guide of this application.


To edit a note, simply press on it.
If you want to save it, click on it again or on another note and a popup saying it was saved will appear.
To add a note, press the + button.
If you want to edit or delete a note, press on the buttons near it.
You can search by entering a term in the search bar.
You can logout by pressing the logout button.


